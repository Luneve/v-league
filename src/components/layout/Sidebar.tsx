const navMap: Record<Role, NavItem[]> = {
  volunteer: volunteerNav,
  organization: orgNav,
  admin: adminNav,
};

interface SidebarProps {
  role: Role;
  collapsed?: boolean;
  onClose?: () => void;
}

function Sidebar({ role, collapsed = false, onClose }: SidebarProps) {
  const pathname = usePathname();
  const items = navMap[role];

  return (
    <aside
      className={`
        flex h-full flex-col bg-surface border-r border-border
        ${collapsed ? "w-16" : "w-64"}
        transition-all duration-200
      `}
    >
      {/* Logo */}
      <div className="flex h-16 items-center gap-3 px-4 border-b border-border">
        <div className="flex h-8 w-8 items-center justify-center rounded-lg bg-accent text-white font-bold text-sm">
          V
        </div>
        {!collapsed && (
          <span className="text-lg font-bold text-text-primary">V League</span>
        )}
        {onClose && (
          <button
            onClick={onClose}
            className="ml-auto text-muted hover:text-text-primary lg:hidden"
            aria-label="Close menu"
          >
            <svg className="h-5 w-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
              <path strokeLinecap="round" strokeLinejoin="round" d="M6 18L18 6M6 6l12 12" />
            </svg>
          </button>
        )}
      </div>

      {/* Nav Items */}
      <nav className="flex-1 space-y-1 px-3 py-4">
        {items.map((item) => {
          const isActive = pathname === item.href || pathname.startsWith(item.href + "/");
          return (
            <Link
              key={item.href}
              href={item.href}
              onClick={onClose}
              className={`
                flex items-center gap-3 rounded-xl px-3 py-2.5 text-sm font-medium
                transition-colors duration-150
                ${
                  isActive
                    ? "bg-accent/10 text-accent border-l-2 border-accent"
                    : "text-muted hover:bg-surface-2 hover:text-text-primary"
                }
              `}
            >
              {item.icon}
              {!collapsed && <span>{item.label}</span>}
            </Link>
          );
        })}
      </nav>
    </aside>
  );
}

export { Sidebar };
export type { SidebarProps };
